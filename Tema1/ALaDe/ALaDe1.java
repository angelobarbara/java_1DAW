/**
Utilizando el for, entrega un programa ALaDeUna que muestre:
A la de 1... 

A la de 2... 

A la de 3....

@author Angelo Barbara
@version 1.0
*/
public class ALaDe1 {
    public static void main(String[] args) {

		for (int i = 0; i < 3; i++)
			System.out.println("A la de "+(i+1)+"..");
		
		
	}
}	
	