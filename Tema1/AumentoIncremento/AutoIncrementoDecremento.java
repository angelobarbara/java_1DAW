public class AutoIncrementoDecremento {
		public static void main(String[] args){
			
			int i = 1;
			System.out.println("i : "+ i); //muestra 1
			System.out.println("i++ : "+ ++i); //muestra 2
			System.out.println("++i : "+ i++); //muestra 2 e incrementa de 1
			System.out.println("i : "+ i); //muestra 3
			System.out.println("--i : "+ --i); //muestra 2
			System.out.println("i-- : "+ i--); //muestra 2 y resta 1
			System.out.println("i : "+ i); //muestra 1


		}
}